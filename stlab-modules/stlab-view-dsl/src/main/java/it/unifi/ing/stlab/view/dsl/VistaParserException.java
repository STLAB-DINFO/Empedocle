package it.unifi.ing.stlab.view.dsl;

public class VistaParserException extends Exception {

	private static final long serialVersionUID = 1L;

	public VistaParserException() {
		super();
	}

	public VistaParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public VistaParserException(String message) {
		super(message);
	}

	public VistaParserException(Throwable cause) {
		super(cause);
	}
}
